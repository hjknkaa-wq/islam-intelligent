class TestGitShaDetection:
    """Tests for git SHA auto-detection."""

    def test_get_git_sha_returns_string_or_none(self):
        """Git SHA should be 40-char hex string or None if not in git repo."""
        sha = get_git_sha()

        if sha is not None:
            assert len(sha) == 40
            assert all(c in "0123456789abcdef" for c in sha)


class TestParamsHash:
    """Tests for parameter hash computation."""

    def test_compute_params_hash_returns_64_char_hex(self):
        """Params hash should be 64-character hex string."""
        params = {"key": "value", "number": 42}
        hash_value = compute_params_hash(params)

        assert len(hash_value) == 64
        assert all(c in "0123456789abcdef" for c in hash_value)

    def test_compute_params_hash_is_deterministic(self):
        """Same params should produce same hash."""
        params = {"b": 2, "a": 1}  # Note: different order
        hash1 = compute_params_hash(params)
        hash2 = compute_params_hash({"a": 1, "b": 2})

        assert hash1 == hash2

    def test_compute_params_hash_different_params_different_hash(self):
        """Different params should produce different hashes."""
        hash1 = compute_params_hash({"key": "value1"})
        hash2 = compute_params_hash({"key": "value2"})

        assert hash1 != hash2


